export type AppIconKey = keyof typeof ICON_REGISTRY;

export const APP_ICON_KEYS = new Set<string>(Object.keys(ICON_REGISTRY));

export const REGISTERED_ICONS = Object.values(ICON_REGISTRY).map((entry) => entry.icon);

/**
 * 🔑 Convierte clave semántica → nombre real de Lucide
 */
export function resolveLucideIconName(name?: string | null): string {
  const key = name?.trim().toLowerCase() as AppIconKey | undefined;

  if (key && ICON_REGISTRY[key]) {
    return ICON_REGISTRY[key].lucideName;
  }

  return ICON_REGISTRY.default.lucideName;
}
